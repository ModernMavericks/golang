#!/bin/sh
set -eu
here="$(cd "$(dirname "$0")" && pwd)"
. "$here/versions.sh"
: "${MSC_SCRIPTS:?mavericks-shared-cmake not found; install it -- see its README}"
command -v go >/dev/null || { echo "FATAL: need a stock arm64 bootstrap go on PATH" >&2; exit 1; }
GOROOT_BOOTSTRAP=$(go env GOROOT); export GOROOT_BOOTSTRAP

# inputs (same patches as native; @SSLDIR@ -> native CA convention path)
SDK=$(sh "$MSC_SCRIPTS/fetch_sdk.sh"); export SDK
[ -d "$SDK" ] || { echo "FATAL: 10.9 SDK not found: '$SDK'" >&2; exit 1; }
sh "$here/fetch-go.sh"
sh "$here/apply-patches.sh"
LEGACY_A=$(sh "$here/build-legacy-support.sh" | tail -1)   # amd64 shim vs the 10.9 SDK (for the apps)
[ -f "$LEGACY_A" ] || { echo "FATAL: legacy-support .a not built: '$LEGACY_A'" >&2; exit 1; }

# Build the patched Go NATIVELY for the arm64 host: no Rosetta, no legacy flags, no min-10.9 on the
# toolchain itself. The 10.9-safety applies to the apps it emits (via the cross CC wrapper below),
# not to the toolchain binaries. Explicitly clear any native-build env that might leak in.
unset GOOS GOARCH GOHOSTOS GOHOSTARCH GOROOT_FINAL \
      GO_LDFLAGS BOOT_GO_LDFLAGS GO_BOOTSTRAP_LDFLAGS CGO_LDFLAGS CGO_CFLAGS GO_EXTLINK_ENABLED
export GOROOT_FINAL="$CROSS_PREFIX"
export GOCACHE="$WORK/.gocache-cross"
( cd "$WORK/go/src" && ./make.bash -v )

# stage into a DESTDIR tree at the cross prefix
stage="$WORK/staging-cross"
rm -rf "$stage"; mkdir -p "$stage$CROSS_PREFIX"
( cd "$WORK/go" && rm -rf pkg/obj pkg/bootstrap && pax -rw . "$stage$CROSS_PREFIX" )

# amd64 shim + the pinned-SDK fetch scripts (self-contained first-use fetch, no shared-cmake at runtime)
mkdir -p "$stage$CROSS_PREFIX/lib" "$stage$CROSS_PREFIX/libexec"
cp "$LEGACY_A" "$stage$CROSS_PREFIX/lib/libMacportsLegacySupport.a"
cp "$MSC_SCRIPTS/fetch_sdk.sh" "$MSC_SCRIPTS/mavericks_fetch.sh" "$stage$CROSS_PREFIX/libexec/"

# Cross CC wrapper: force the amd64/10.9 target on every invocation (Go doesn't inject -arch on
# darwin, so we must), and add the static shim + -Wl,-U allowances on LINK steps only.
_wlu=""; for s in $WLU_SYMS; do _wlu="$_wlu \\
  -Wl,-U,$s"; done
cat > "$stage$CROSS_PREFIX/bin/mavericks-cross-clang" <<EOF
#!/bin/sh
# Cross CC: build darwin/amd64 min-10.9 apps on a modern arm64 host. Resolves the pinned 10.9 SDK on
# first use (cached), forces target arch/sdk/min-version on ALL steps, adds the legacy shim + -Wl,-U
# on link steps. Generated by build-cross.sh. Self-locating so it works staged OR installed.
PREFIX="\$(cd "\$(dirname "\$0")/.." && pwd)"
SDK=\$(sh "\$PREFIX/libexec/fetch_sdk.sh")
TARGET="-arch x86_64 -isysroot \$SDK -mmacosx-version-min=10.9"
for a in "\$@"; do
  case "\$a" in -c|-E|-S) exec /usr/bin/clang \$TARGET "\$@" ;; esac
done
exec /usr/bin/clang \$TARGET "\$@" \\
  "\$PREFIX/lib/libMacportsLegacySupport.a"$_wlu
EOF
chmod 755 "$stage$CROSS_PREFIX/bin/mavericks-cross-clang"

# go.env defaults: target 10.9/amd64 out of the box; the wrapper handles the link.
{
  echo "GOOS=darwin"
  echo "GOARCH=amd64"
  echo "CGO_ENABLED=1"
  echo "CC=$CROSS_PREFIX/bin/mavericks-cross-clang"
} >> "$stage$CROSS_PREFIX/go.env"

test -x "$stage$CROSS_PREFIX/bin/go" || { echo "FATAL: no bin/go staged" >&2; exit 1; }
echo "$stage$CROSS_PREFIX"
